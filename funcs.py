def add(a, b):
    """
    Складывает два числа.

    Args:
        a: Первое число.
        b: Второе число.

    Returns:
        Сумму двух чисел.
    """
    return a + b


def subtract(a, b):
    """
    Вычитает второе число из первого.

    Args:
        a: Первое число.
        b: Второе число.

    Returns:
        Разность двух чисел.
    """
    return a - b
